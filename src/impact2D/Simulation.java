    private class Animation extends Thread{
        private boolean interrupted = false, pause = true;
        public final int f = 60;
        private final Object waiter = new Object();
        @Override
        public void run(){
            while(!interrupted){
                if(pause)
                    try {
                        synchronized(waiter){
                            waiter.wait();
                        }
                    }catch(InterruptedException ex){}
                
                double dx = p2.x-p1.x, dy = p2.y-p1.y, Dx = Math.sqrt(dx*dx+dy*dy);
                if(Dx <= (p1.r+p2.r)){
                    double dvx = p1.vx-p2.vx, dvy = p1.vy-p2.vy;
                    double s = dx*dvx+dy*dvy;
                    if(s > 0)
                        p1.collideWith(p2, dx,dy,Dx,s);
                }
                if((p1.x-p1.r) <= 0 || (p1.x+p1.r) >= boxw)
                    p1.vx *= -1;
                if((p1.y-p1.r) <= 0 || (p1.y+p1.r) >= boxh)
                    p1.vy *= -1;
                if((p2.x-p2.r) <= 0 || (p2.x+p2.r) >= boxw)
                    p2.vx *= -1;
                if((p2.y-p2.r) <= 0 || (p2.y+p2.r) >= boxh)
                    p2.vy *= -1;
                p1.next();
                p2.next();
                java.awt.EventQueue.invokeLater(new Runnable() {
                        public void run() {
                            updateGUI();
                        }
                    });
                try{
                    sleep(1000/f);
                }catch(InterruptedException ex){}
            }
        }
        public void pause(){
            pause = true;
        }
        public void unpause(){
            pause = false;
            synchronized(waiter){
                waiter.notify();
            }
        }
        public boolean paused(){
            return pause;
        }
        public void nextFrame(){
            synchronized(waiter){
                waiter.notify();
            }
        }
        @Override
        public void interrupt(){
            interrupted = true;
        }
        @Override
        public boolean isInterrupted(){
            return interrupted;
        }
    }
