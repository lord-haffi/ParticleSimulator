    public void resetTo(double[] val1, double[] val2, int bw, int bh){
        playing.interrupt();
        if(playing.paused()){
            playing.unpause();
        }
        while(playing.isAlive()){}
        boxw = bw;
        boxh = bh;
        p1.setTo(val1);
        p2.setTo(val2);
        playing = new Animation();
        playing.start();
        jTextField11.setText(boxw+"");
        jTextField12.setText(boxh+"");
        jTextField9.setText(p1.r+"");
        jTextField10.setText(p2.r+"");
        updateGUI();
        jButton1.setText("Play");
    }
